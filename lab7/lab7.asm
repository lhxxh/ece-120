; This program is  to render a single ASCII character in 
; a large (8x16-pixel) font to the monitor.
; This purpose can be achieved by calculating the address, 
; use pointer register to point memory locations,
; ,tracing every bit with column register and row register,   
; using traps for output to print desired ASCII character.
 
; R1 is used to calculate the absoulate address of sprite.
; R2 is used as row counter, which initially sets to 16.
; R3 is used as column counter, which initially sets to 8.
; R4 is to used to store the value of R1.
; R5 is to used to store the value x8000
; R6 is used as calculate R4 AND R5


;START  

                  .ORIG x3000;                        The programs starts at x3000 memory location.

;Initilization

                  LD  R5, NUMBER                      ;Load R5 with number x8000
                  AND R2, R2, #0                      ;CLear R2 with #0
                  AND R3, R3, #0                      ;Clear R3 with #0

;Calculate the address

                  LDI R1, ASCII_CHARACTER             ;Load R1 with the ASCII character 
                  AND R6, R6, #0                      ;clear R6 with #0
                  ADD R6, R6, #4                      ;Load #4 into R6 as a 4-times left shift counter
LOOP_MULTIPLY     ADD R1, R1, R1                      ;multiply R1 with 2 each time
                  ADD R6, R6, #-1                     ;decreament counter with 1 each time
                  BRp LOOP_MULTIPLY                   ;go back in order to execute multiply 16
                  LEA R4, FONT_DATA                   ;Load FONT_DATA string's first line address into R4
                  ADD R1, R1, R4                      ;Calculate the absoulate address of the ASCII character in R1

;set the row and column counter
            
                  ADD R2, R2, #15                     ;Set R2, row counter, to #15
LOOP_ROW          LDR R4, R1, #0                      ;Load the content of the address into R4
                  ADD R2, R2, #0                      ;Set R2 condition for check
                  BRn FINISH                          ;If R2 equals 0, jump to FINISH label
                  ADD R3, R3, #8                      ;Set R3, column counter, to #8
LOOP_COLUMN       ADD R3, R3, #0                      ;Set R3 condition for check
                  BRz FINISH_COLUMN                   ;If R3 equals 0, jump to FINISH_COLUMN label

;Printing decision

                  AND R6, R5, R4                      ;Set condtion in R6 for check
                  BRn POSITIVE_ONE                    ;If the value stored in R6 is #8000, jump to POSITIVE_ONE label
                  LDI R0, CHARACTER_ZERO              ;In the value is zero case, load CHARACTER_ZERO (x5000) to R0
                  OUT                                 ;PRINT the ASCII character on screen
                  BRnzp JUMP                          ;unconditional jump to JUMP label
POSITIVE_ONE      LDI R0, CHARACTER_ONE               ;In the value is one case, load CHARACTER_ONE (x5001) to R0
                  OUT                                 ;PRINT the ASCII character on screen
             
;Refresh the counters

JUMP              ADD R3, R3, #-1                     ;Decrement R3, the column register, by one
                  ADD R4, R4, R4                      ;SHIFT the value in R4 left by one
                  BRnzp LOOP_COLUMN                   ;Unconditional jump to LOOP_COLUMN label
                  
;Add new line after finishing column

FINISH_COLUMN     LD R0, ASCII_NL                     ;Load ASCII character new line into R0
                  OUT                                 ;Print the new line on the screen
                  
;Refresh the counters

                  ADD R2, R2, #-1                     ;Decrement R2, the row counter, by one
                  ADD R1, R1, #1                      ;Increment R1, the pointer register, by one 
                  BRnzp LOOP_ROW                      ;Unconditional jump to LOOP_ROW label
          
;FINISH

FINISH            HALT                                ;Everthing has been done.

NUMBER           .FILL x8000
ASCII_CHARACTER  .FILL x5002
CHARACTER_ZERO   .FILL x5000
CHARACTER_ONE    .FILL x5001                                 
ASCII_NL         .FILL x0A

             
